import { useState, useEffect, useCallback } from 'react';
import { Copy, Check, Trash2, Code, Braces, AlertCircle, Info, Download, Terminal } from 'lucide-react';
import { useTranslation } from 'react-i18next';

const MAX_LENGTH = 100000;
const MAX_DEPTH = 15;

type LibraryType = 'system' | 'newtonsoft';

interface CSharpClass {
  name: string;
  fields: { name: string; type: string; originalKey: string }[];
}

export function JSONToCSharp({ initialData, onStateChange }: { initialData?: any; onStateChange?: (state: any) => void }) {
  const { t } = useTranslation();
  const [jsonInput, setJsonInput] = useState(initialData?.jsonInput || '');
  const [output, setOutput] = useState('');
  const [error, setError] = useState<string | null>(null);
  const [copied, setCopied] = useState(false);
  const [library, setLibrary] = useState<LibraryType>(initialData?.library || 'system');

  useEffect(() => {
    onStateChange?.({ jsonInput, library });
  }, [jsonInput, library, onStateChange]);

  const toPascalCase = (str: string) => {
    return str
      .replace(/[^a-z0-9]/gi, ' ')
      .split(' ')
      .filter(Boolean)
      .map(word => word.charAt(0).toUpperCase() + word.slice(1).toLowerCase())
      .join('') || 'AutoGenerated';
  };

  const generateCSharp = useCallback(() => {
    if (!jsonInput.trim()) {
      setOutput('');
      setError(null);
      return;
    }

    if (jsonInput.length > MAX_LENGTH) {
      setError(t('error.max_length', { max: MAX_LENGTH.toLocaleString() }));
      setOutput('');
      return;
    }

    try {
      const parsed = JSON.parse(jsonInput);
      const classes: CSharpClass[] = [];
      const classNames = new Set<string>();

      const getCSharpType = (val: any, fieldName: string, depth: number): string => {
        if (val === null || val === undefined) return 'object';
        if (depth > MAX_DEPTH) return 'object';

        if (Array.isArray(val)) {
          const itemType = val.length > 0 ? getCSharpType(val[0], fieldName, depth + 1) : 'object';
          return `List<${itemType}>`;
        }

        if (typeof val === 'object') {
          let className = toPascalCase(fieldName);
          if (className === 'Root') className = 'RootObject';
          if (className === 'List' || className === 'Object') className += 'Item';

          let finalName = className;
          let counter = 1;
          while (classNames.has(finalName)) {
            finalName = className + counter++;
          }

          const fields = Object.entries(val).map(([key, value]) => ({
            name: toPascalCase(key),
            type: getCSharpType(value, key, depth + 1),
            originalKey: key
          }));

          classes.push({ name: finalName, fields });
          classNames.add(finalName);
          return finalName;
        }

        if (typeof val === 'number') return Number.isInteger(val) ? 'int' : 'double';
        if (typeof val === 'boolean') return 'bool';
        return 'string';
      };

      getCSharpType(parsed, 'Root', 0);

      let finalOutput = '';
      if (library === 'system') {
        finalOutput += 'using System.Text.Json.Serialization;\n';
      } else {
        finalOutput += 'using Newtonsoft.Json;\n';
      }
      finalOutput += 'using System.Collections.Generic;\n\n';

      classes.reverse().forEach((csClass) => {
        finalOutput += `public class ${csClass.name}\n{\n`;

        csClass.fields.forEach(field => {
          if (library === 'system') {
            finalOutput += `    [JsonPropertyName("${field.originalKey}")]\n`;
          } else {
            finalOutput += `    [JsonProperty("${field.originalKey}")]\n`;
          }
          finalOutput += `    public ${field.type} ${field.name} { get; set; }\n\n`;
        });

        finalOutput = finalOutput.trimEnd() + '\n}\n\n';
      });

      setOutput(finalOutput.trim());
      setError(null);
    } catch (e) {
      setError(t('error.invalid_json'));
      setOutput('');
    }
  }, [jsonInput, library, t]);

  useEffect(() => {
    generateCSharp();
  }, [generateCSharp]);

  const handleConvert = () => {
    generateCSharp();
  };

  const handleCopy = () => {
    if (!output) return;
    navigator.clipboard.writeText(output);
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  const handleClear = () => {
    setJsonInput('');
    setOutput('');
    setError(null);
  };

  const handleDownload = () => {
    if (!output) return;
    const blob = new Blob([output], { type: 'text/plain' });
    const url = URL.createObjectURL(blob);
    const link = document.createElement('a');
    link.href = url;
    link.download = 'CSharpClasses.cs';
    link.click();
    URL.revokeObjectURL(url);
  };

  return (
    <div className="max-w-6xl mx-auto space-y-8">
      <div className="flex flex-wrap gap-4 justify-between items-center bg-slate-50 dark:bg-slate-900/50 p-6 rounded-[2rem] border border-slate-200 dark:border-slate-800">
        <div className="flex flex-wrap gap-2">
          {(['system', 'newtonsoft'] as LibraryType[]).map((lib) => (
            <button
              key={lib}
              onClick={() => setLibrary(lib)}
              className={`px-4 py-2 rounded-xl text-sm font-bold transition-all focus-visible:ring-2 focus-visible:ring-indigo-500 focus-visible:outline-none ${
                library === lib
                  ? 'bg-indigo-600 text-white shadow-lg shadow-indigo-600/20'
                  : 'bg-white dark:bg-slate-800 text-slate-500 hover:text-slate-900 dark:hover:text-slate-200'
              }`}
            >
              {lib === 'system' ? 'System.Text.Json' : 'Newtonsoft.Json'}
            </button>
          ))}
        </div>
      </div>

      <div className="grid grid-cols-1 lg:grid-cols-2 gap-8">
        <div className="space-y-4">
          <div className="flex justify-between items-center px-1">
            <label htmlFor="json-input" className="text-xs font-black uppercase tracking-widest text-slate-400 flex items-center gap-2">
              <Braces className="w-4 h-4 text-indigo-500" /> {t('common.input')} (JSON)
            </label>
            <button
              onClick={handleClear}
              disabled={!jsonInput}
              className="text-xs font-bold text-rose-500 bg-rose-50 dark:bg-rose-500/10 hover:bg-rose-100 dark:hover:bg-rose-500/20 px-3 py-1.5 rounded-xl flex items-center gap-1 transition-all disabled:opacity-50 disabled:cursor-not-allowed focus-visible:ring-2 focus-visible:ring-rose-500 focus-visible:outline-none"
            >
              <Trash2 className="w-3 h-3" /> {t('common.clear')}
            </button>
          </div>
          <textarea
            id="json-input"
            value={jsonInput}
            onChange={(e) => setJsonInput(e.target.value)}
            onKeyDown={(e) => {
              if (e.key === 'Enter' && (e.ctrlKey || e.metaKey)) {
                handleConvert();
              }
            }}
            placeholder='{"name": "John", "age": 30, "address": {"city": "New York"}}'
            className={`w-full h-[500px] p-6 bg-slate-50 dark:bg-slate-900 border ${error ? 'border-rose-500' : 'border-slate-200 dark:border-slate-800'} rounded-3xl outline-none focus:ring-2 ${error ? 'focus:ring-rose-500/20' : 'focus:ring-indigo-500/20'} transition-all font-mono text-sm leading-relaxed dark:text-slate-300 resize-none`}
          />
        </div>

        <div className="space-y-4">
          <div className="flex justify-between items-center px-1">
            <label htmlFor="cs-output" className="text-xs font-black uppercase tracking-widest text-slate-400 flex items-center gap-2">
              <Terminal className="w-4 h-4 text-indigo-500" /> C# Class
            </label>
            <div className="flex gap-2">
              <button
                onClick={handleDownload}
                disabled={!output}
                className="text-xs font-bold px-3 py-1 rounded-full text-indigo-500 bg-indigo-50 dark:bg-indigo-500/10 hover:bg-indigo-100 transition-all flex items-center gap-1 disabled:opacity-50"
              >
                <Download className="w-3 h-3" /> {t('common.download')}
              </button>
              <button
                onClick={handleCopy}
                disabled={!output}
                className={`text-xs font-bold px-4 py-1.5 rounded-xl transition-all border focus-visible:ring-2 focus-visible:ring-indigo-500 focus-visible:outline-none flex items-center gap-2 ${
                  copied
                    ? 'bg-emerald-50 dark:bg-emerald-500/10 text-emerald-600 dark:text-emerald-400 border-emerald-200 dark:border-emerald-500/20'
                    : 'bg-white dark:bg-slate-800 text-indigo-600 dark:text-indigo-400 border-slate-200 dark:border-slate-700 hover:border-indigo-500/50'
                } disabled:opacity-50 disabled:cursor-not-allowed`}
              >
                {copied ? <Check className="w-3 h-3" /> : <Copy className="w-3 h-3" />}
                {copied ? t('common.copied') : t('common.copy')}
              </button>
            </div>
          </div>
          <textarea
            id="cs-output"
            value={output}
            readOnly
            placeholder="C# class definitions will appear here..."
            className="w-full h-[500px] p-6 bg-slate-900 border border-slate-800 rounded-3xl outline-none font-mono text-sm leading-relaxed text-indigo-300 resize-none"
          />
        </div>
      </div>

      {error && (
        <div className="bg-rose-50 dark:bg-rose-500/10 border border-rose-200 dark:border-rose-800 p-4 rounded-2xl flex items-center gap-3 text-rose-600 dark:text-rose-400 font-bold animate-in fade-in slide-in-from-top-2">
          <AlertCircle className="w-5 h-5" />
          {error}
        </div>
      )}

      {/* Info */}
      <div className="bg-indigo-50 dark:bg-indigo-900/10 p-8 rounded-[2.5rem] border border-indigo-100 dark:border-indigo-900/20 flex items-start gap-4">
        <div className="p-3 bg-white dark:bg-slate-800 text-indigo-600 rounded-2xl shadow-sm border border-slate-100 dark:border-slate-700">
          <Info className="w-6 h-6" />
        </div>
        <div className="space-y-2">
          <h4 className="font-bold dark:text-white">{t('jsontocsharp.about_title')}</h4>
          <p className="text-sm text-slate-500 dark:text-slate-400 leading-relaxed">
            {t('jsontocsharp.about_text')}
          </p>
        </div>
      </div>
    </div>
  );
}
