export function EmojiPicker() {
  const [search, setSearch] = useState('');
  const [selectedCategory, setSelectedCategory] = useState<string | null>(null);
  const [copied, setCopied] = useState<string | null>(null);

  const filteredEmojis = useMemo(() => {
    const query = search.toLowerCase();
    return EMOJI_DATA.map(cat => ({
      ...cat,
      emojis: cat.emojis.filter(emoji =>
        !query ||
        cat.category.toLowerCase().includes(query) ||
        emoji.includes(query)
      )
    })).filter(cat => cat.emojis.length > 0);
  }, [search]);

  const copyEmoji = (emoji: string) => {
    navigator.clipboard?.writeText(emoji);
    setCopied(emoji);
    setTimeout(() => setCopied(null), 2000);
  };

  return (
    <div className="max-w-5xl mx-auto space-y-8">
      <div className="flex flex-col md:flex-row gap-6 items-center justify-between bg-slate-50 dark:bg-slate-900/50 p-8 rounded-[2.5rem] border border-slate-200 dark:border-slate-800">
        <div className="w-full md:w-96 relative group">
          <Search className="absolute left-4 top-1/2 -translate-y-1/2 w-5 h-5 text-slate-400" />
          <input
            type="text"
            value={search}
            onChange={(e) => setSearch(e.target.value)}
            placeholder="Rechercher un emoji..."
            className="w-full pl-12 pr-4 py-4 bg-white dark:bg-slate-900 border border-slate-200 dark:border-slate-800 rounded-2xl outline-none focus:ring-2 focus:ring-indigo-500/20 transition-all dark:text-white font-medium"
          />
        </div>

        <div className="flex gap-2 overflow-x-auto no-scrollbar pb-2 md:pb-0 w-full md:w-auto">
          <button
            onClick={() => setSelectedCategory(null)}
            className={`px-4 py-2 rounded-full text-xs font-black transition-all whitespace-nowrap border ${
              selectedCategory === null ? 'bg-indigo-600 border-indigo-600 text-white' : 'bg-white dark:bg-slate-800 text-slate-400 border-slate-200 dark:border-slate-800'
            }`}
          >
            Tous
          </button>
          {EMOJI_DATA.map(cat => (
            <button
              key={cat.category}
              onClick={() => setSelectedCategory(cat.category)}
              className={`flex items-center gap-2 px-4 py-2 rounded-full text-xs font-black transition-all whitespace-nowrap border ${
                selectedCategory === cat.category ? 'bg-indigo-600 border-indigo-600 text-white' : 'bg-white dark:bg-slate-800 text-slate-400 border-slate-200 dark:border-slate-800'
              }`}
            >
              <cat.icon className="w-3 h-3" />
              {cat.category}
            </button>
          ))}
        </div>
      </div>

      <div className="space-y-12 h-[600px] overflow-y-auto pr-4 custom-scrollbar">
        {filteredEmojis.map(cat => {
          if (selectedCategory && cat.category !== selectedCategory) return null;
          return (
            <section key={cat.category} className="space-y-4">
              <div className="flex items-center gap-2 sticky top-0 bg-white/80 dark:bg-slate-950/80 backdrop-blur-md py-2 z-10">
                <cat.icon className="w-4 h-4 text-indigo-500" />
                <h3 className="text-xs font-black uppercase tracking-widest text-slate-400">{cat.category}</h3>
              </div>
              <div className="grid grid-cols-6 sm:grid-cols-8 md:grid-cols-10 lg:grid-cols-12 gap-2">
                {cat.emojis.map(emoji => (
                  <button
                    key={emoji}
                    onClick={() => copyEmoji(emoji)}
                    className="aspect-square flex items-center justify-center text-3xl hover:bg-slate-100 dark:hover:bg-slate-800 rounded-2xl transition-all relative group"
                    title="Copier"
                  >
                    <span className="group-hover:scale-125 transition-transform duration-200">{emoji}</span>
                    {copied === emoji && (
                      <div className="absolute -top-8 left-1/2 -translate-x-1/2 bg-emerald-500 text-white text-[10px] font-bold px-2 py-1 rounded-md animate-in fade-in slide-in-from-bottom-2">
                        Copié !
                      </div>
                    )}
                  </button>
                ))}
              </div>
            </section>
          );
        })}

        {filteredEmojis.length === 0 && (
          <div className="h-full flex flex-col items-center justify-center text-center space-y-4">
            <div className="w-16 h-16 bg-slate-50 dark:bg-slate-900 rounded-3xl flex items-center justify-center text-slate-300">
              <Smile className="w-8 h-8" />
            </div>
            <p className="text-slate-500 dark:text-slate-400 font-bold">Aucun emoji trouvé pour "{search}"</p>
          </div>
        )}
      </div>
    </div>
  );
}
