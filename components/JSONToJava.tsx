import { useState, useEffect, useCallback } from 'react';
import { Copy, Check, Trash2, Code, Braces, AlertCircle, Info, Download, Terminal } from 'lucide-react';
import { useTranslation } from 'react-i18next';

const MAX_LENGTH = 100000;
const MAX_DEPTH = 15;

type AnnotationType = 'none' | 'jackson' | 'gson';

interface JavaClass {
  name: string;
  fields: { name: string; type: string; originalKey: string }[];
}

export function JSONToJava({ initialData, onStateChange }: { initialData?: any; onStateChange?: (state: any) => void }) {
  const { t } = useTranslation();
  const [jsonInput, setJsonInput] = useState(initialData?.jsonInput || '');
  const [output, setOutput] = useState('');
  const [error, setError] = useState<string | null>(null);
  const [copied, setCopied] = useState(false);
  const [annotation, setAnnotation] = useState<AnnotationType>(initialData?.annotation || 'none');
  const [useLombok, setUseLombok] = useState(initialData?.useLombok ?? false);

  useEffect(() => {
    onStateChange?.({ jsonInput, annotation, useLombok });
  }, [jsonInput, annotation, useLombok, onStateChange]);

  const toPascalCase = (str: string) => {
    return str
      .replace(/[^a-z0-9]/gi, ' ')
      .split(' ')
      .filter(Boolean)
      .map(word => word.charAt(0).toUpperCase() + word.slice(1).toLowerCase())
      .join('') || 'AutoGenerated';
  };

  const toCamelCase = (str: string) => {
    const pascal = toPascalCase(str);
    return pascal.charAt(0).toLowerCase() + pascal.slice(1);
  };

  const generateJava = useCallback(() => {
    if (!jsonInput.trim()) {
      setOutput('');
      setError(null);
      return;
    }

    if (jsonInput.length > MAX_LENGTH) {
      setError(t('error.max_length', { max: MAX_LENGTH.toLocaleString() }));
      setOutput('');
      return;
    }

    try {
      const parsed = JSON.parse(jsonInput);
      const classes: JavaClass[] = [];
      const classNames = new Set<string>();

      const getJavaType = (val: any, fieldName: string, depth: number): string => {
        if (val === null || val === undefined) return 'Object';
        if (depth > MAX_DEPTH) return 'Object';

        if (Array.isArray(val)) {
          const itemType = val.length > 0 ? getJavaType(val[0], fieldName, depth + 1) : 'Object';
          return `List<${itemType}>`;
        }

        if (typeof val === 'object') {
          let className = toPascalCase(fieldName);
          if (className === 'Root') className = 'RootObject';
          if (className === 'List' || className === 'Object') className += 'Item';

          // Basic de-duplication
          let finalName = className;
          let counter = 1;
          while (classNames.has(finalName)) {
            finalName = className + counter++;
          }

          const fields = Object.entries(val).map(([key, value]) => {
            let name = toCamelCase(key);
            // Java keywords
            const keywords = ['abstract', 'assert', 'boolean', 'break', 'byte', 'case', 'catch', 'char', 'class', 'const', 'continue', 'default', 'do', 'double', 'else', 'enum', 'extends', 'final', 'finally', 'float', 'for', 'goto', 'if', 'implements', 'import', 'instanceof', 'int', 'interface', 'long', 'native', 'new', 'package', 'private', 'protected', 'public', 'return', 'short', 'static', 'strictfp', 'super', 'switch', 'synchronized', 'this', 'throw', 'throws', 'transient', 'try', 'void', 'volatile', 'while', 'true', 'false', 'null'];
            if (keywords.includes(name) || /^[0-9]/.test(name)) {
              name = '_' + name;
            }
            return {
              name: name,
              type: getJavaType(value, key, depth + 1),
              originalKey: key
            };
          });

          classes.push({ name: finalName, fields });
          classNames.add(finalName);
          return finalName;
        }

        if (typeof val === 'number') return Number.isInteger(val) ? 'Integer' : 'Double';
        if (typeof val === 'boolean') return 'Boolean';
        return 'String';
      };

      getJavaType(parsed, 'Root', 0);

      let finalOutput = '';
      if (annotation === 'jackson') {
        finalOutput += 'import com.fasterxml.jackson.annotation.JsonProperty;\n';
      } else if (annotation === 'gson') {
        finalOutput += 'import com.google.gson.annotations.SerializedName;\n';
      }

      finalOutput += 'import java.util.List;\n';
      if (useLombok) {
        finalOutput += 'import lombok.Data;\n';
      }
      finalOutput += '\n';

      classes.reverse().forEach((javaClass) => {
        if (useLombok) {
          finalOutput += '@Data\n';
        }
        finalOutput += `public class ${javaClass.name} {\n`;

        javaClass.fields.forEach(field => {
          if (annotation === 'jackson') {
            finalOutput += `    @JsonProperty("${field.originalKey}")\n`;
          } else if (annotation === 'gson') {
            finalOutput += `    @SerializedName("${field.originalKey}")\n`;
          }
          finalOutput += `    private ${field.type} ${field.name};\n`;
        });

        if (!useLombok) {
          javaClass.fields.forEach(field => {
            const capitalized = field.name.charAt(0).toUpperCase() + field.name.slice(1);
            // Getter
            finalOutput += `\n    public ${field.type} get${capitalized}() {\n`;
            finalOutput += `        return ${field.name};\n`;
            finalOutput += `    }\n`;
            // Setter
            finalOutput += `\n    public void set${capitalized}(${field.type} ${field.name}) {\n`;
            finalOutput += `        this.${field.name} = ${field.name};\n`;
            finalOutput += `    }\n`;
          });
        }

        finalOutput += '}\n\n';
      });

      setOutput(finalOutput.trim());
      setError(null);
    } catch (e) {
      setError(t('error.invalid_json'));
      setOutput('');
    }
  }, [jsonInput, annotation, useLombok, t]);

  useEffect(() => {
    handleConvert();
  }, [jsonInput, annotation, useLombok]);

  const handleConvert = () => {
    generateJava();
  };

  const handleCopy = () => {
    if (!output) return;
    navigator.clipboard.writeText(output);
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  const handleClear = () => {
    setJsonInput('');
    setOutput('');
    setError(null);
  };

  const handleDownload = () => {
    if (!output) return;
    const blob = new Blob([output], { type: 'text/plain' });
    const url = URL.createObjectURL(blob);
    const link = document.createElement('a');
    link.href = url;
    link.download = 'JavaClasses.java';
    link.click();
    URL.revokeObjectURL(url);
  };

  return (
    <div className="max-w-6xl mx-auto space-y-8">
      <div className="flex flex-wrap gap-4 justify-between items-center bg-slate-50 dark:bg-slate-900/50 p-6 rounded-[2rem] border border-slate-200 dark:border-slate-800">
        <div className="flex flex-wrap gap-2">
          {(['none', 'jackson', 'gson'] as AnnotationType[]).map((type) => (
            <button
              key={type}
              onClick={() => setAnnotation(type)}
              className={`px-4 py-2 rounded-xl text-sm font-bold transition-all focus-visible:ring-2 focus-visible:ring-indigo-500 focus-visible:outline-none ${
                annotation === type
                  ? 'bg-indigo-600 text-white shadow-lg shadow-indigo-600/20'
                  : 'bg-white dark:bg-slate-800 text-slate-500 hover:text-slate-900 dark:hover:text-slate-200'
              }`}
            >
              {t(`jsontojava.${type}`)}
            </button>
          ))}
        </div>
        <button
          onClick={() => setUseLombok(!useLombok)}
          className={`flex items-center gap-2 px-4 py-2 rounded-xl text-sm font-bold transition-all focus-visible:ring-2 focus-visible:ring-indigo-500 focus-visible:outline-none ${
            useLombok
              ? 'bg-emerald-600 text-white shadow-lg shadow-emerald-600/20'
              : 'bg-white dark:bg-slate-800 text-slate-500 hover:text-slate-900 dark:hover:text-slate-200'
          }`}
        >
          {useLombok ? <Check className="w-4 h-4" /> : null} {t('jsontojava.lombok')}
        </button>
      </div>

      <div className="grid grid-cols-1 lg:grid-cols-2 gap-8">
        <div className="space-y-4">
          <div className="flex justify-between items-center px-1">
            <label htmlFor="json-input" className="text-xs font-black uppercase tracking-widest text-slate-400 flex items-center gap-2">
              <Braces className="w-4 h-4 text-indigo-500" /> {t('common.input')} (JSON)
            </label>
            <button
              onClick={handleClear}
              disabled={!jsonInput}
              className="text-xs font-bold text-rose-500 bg-rose-50 dark:bg-rose-500/10 hover:bg-rose-100 dark:hover:bg-rose-500/20 px-3 py-1.5 rounded-xl flex items-center gap-1 transition-all disabled:opacity-50 disabled:cursor-not-allowed focus-visible:ring-2 focus-visible:ring-rose-500 focus-visible:outline-none"
            >
              <Trash2 className="w-3 h-3" /> {t('common.clear')}
            </button>
          </div>
          <textarea
            id="json-input"
            value={jsonInput}
            onChange={(e) => setJsonInput(e.target.value)}
            onKeyDown={(e) => {
              if (e.key === 'Enter' && (e.ctrlKey || e.metaKey)) {
                handleConvert();
              }
            }}
            placeholder='{"name": "John", "age": 30, "address": {"city": "New York"}}'
            className={`w-full h-[500px] p-6 bg-slate-50 dark:bg-slate-900 border ${error ? 'border-rose-500' : 'border-slate-200 dark:border-slate-800'} rounded-3xl outline-none focus:ring-2 ${error ? 'focus:ring-rose-500/20' : 'focus:ring-indigo-500/20'} transition-all font-mono text-sm leading-relaxed dark:text-slate-300 resize-none`}
          />
        </div>

        <div className="space-y-4">
          <div className="flex justify-between items-center px-1">
            <label htmlFor="java-output" className="text-xs font-black uppercase tracking-widest text-slate-400 flex items-center gap-2">
              <Terminal className="w-4 h-4 text-indigo-500" /> Java POJO
            </label>
            <div className="flex gap-2">
              <button
                onClick={handleDownload}
                disabled={!output}
                className="text-xs font-bold px-3 py-1 rounded-full text-indigo-500 bg-indigo-50 dark:bg-indigo-500/10 hover:bg-indigo-100 transition-all flex items-center gap-1 disabled:opacity-50"
              >
                <Download className="w-3 h-3" /> {t('common.download')}
              </button>
              <button
                onClick={handleCopy}
                disabled={!output}
                className={`text-xs font-bold px-4 py-1.5 rounded-xl transition-all border focus-visible:ring-2 focus-visible:ring-indigo-500 focus-visible:outline-none flex items-center gap-2 ${
                  copied
                    ? 'bg-emerald-50 dark:bg-emerald-500/10 text-emerald-600 dark:text-emerald-400 border-emerald-200 dark:border-emerald-500/20'
                    : 'bg-white dark:bg-slate-800 text-indigo-600 dark:text-indigo-400 border-slate-200 dark:border-slate-700 hover:border-indigo-500/50'
                } disabled:opacity-50 disabled:cursor-not-allowed`}
              >
                {copied ? <Check className="w-3 h-3" /> : <Copy className="w-3 h-3" />}
                {copied ? t('common.copied') : t('common.copy')}
              </button>
            </div>
          </div>
          <textarea
            id="java-output"
            value={output}
            readOnly
            placeholder="Java class definitions will appear here..."
            className="w-full h-[500px] p-6 bg-slate-900 border border-slate-800 rounded-3xl outline-none font-mono text-sm leading-relaxed text-indigo-300 resize-none"
          />
        </div>
      </div>

      {error && (
        <div className="bg-rose-50 dark:bg-rose-500/10 border border-rose-200 dark:border-rose-800 p-4 rounded-2xl flex items-center gap-3 text-rose-600 dark:text-rose-400 font-bold animate-in fade-in slide-in-from-top-2">
          <AlertCircle className="w-5 h-5" />
          {error}
        </div>
      )}

      {/* Info */}
      <div className="bg-indigo-50 dark:bg-indigo-900/10 p-8 rounded-[2.5rem] border border-indigo-100 dark:border-indigo-900/20 flex items-start gap-4">
        <div className="p-3 bg-white dark:bg-slate-800 text-indigo-600 rounded-2xl shadow-sm border border-slate-100 dark:border-slate-700">
          <Info className="w-6 h-6" />
        </div>
        <div className="space-y-2">
          <h4 className="font-bold dark:text-white">{t('jsontojava.about_title')}</h4>
          <p className="text-sm text-slate-500 dark:text-slate-400 leading-relaxed">
            {t('jsontojava.about_text')}
          </p>
        </div>
      </div>
    </div>
  );
}
